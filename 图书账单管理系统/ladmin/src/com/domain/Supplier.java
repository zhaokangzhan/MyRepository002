package com.domain;

public class Supplier {
	
	
	private int id;	
	private String suppliername;
	private String suppliercertificate	;
	private String supplierpnumber;
	private String supplieremail;
	private String supplieraddress;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getSuppliername() {
		return suppliername;
	}
	public void setSuppliername(String suppliername) {
		this.suppliername = suppliername;
	}
	public String getSuppliercertificate() {
		return suppliercertificate;
	}
	public void setSuppliercertificate(String suppliercertificate) {
		this.suppliercertificate = suppliercertificate;
	}
	public String getSupplierpnumber() {
		return supplierpnumber;
	}
	public void setSupplierpnumber(String supplierpnumber) {
		this.supplierpnumber = supplierpnumber;
	}
	public String getSupplieremail() {
		return supplieremail;
	}
	public void setSupplieremail(String supplieremail) {
		this.supplieremail = supplieremail;
	}
	public String getSupplieraddress() {
		return supplieraddress;
	}
	public void setSupplieraddress(String supplieraddress) {
		this.supplieraddress = supplieraddress;
	}
	
	
	
	
	
}
